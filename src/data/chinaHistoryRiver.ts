const EVENT_TYPE_LABELS: Readonly<Record<HistoricalEventType, string>> = {
  war: '战争',
  culture: '文化',
  politics: '政治',
  science: '科技',
}

type HistoricalEventWithDescription = IHistoricalEvent & {
  readonly description: string
}

function formatEventYear(year: number): string {
  return year < 0 ? `公元前${Math.abs(year)}年` : `${year}年`
}

function createEventDescription(
  event: IHistoricalEvent,
  dynasties: readonly IDynasty[],
): string {
  const eventStatement = `${formatEventYear(event.year)}，${event.title}。`
  const eventType = EVENT_TYPE_LABELS[event.type]
  const activeDynasties = dynasties.filter(
    (dynasty) =>
      dynasty.startYear <= event.year && event.year < dynasty.endYear,
  )

  if (activeDynasties.length === 0) {
    return `${eventStatement}这是一项${eventType}事件。`
  }

  if (activeDynasties.length === 1) {
    return `${eventStatement}这是一项发生于${activeDynasties[0].chineseName}时期的${eventType}事件。`
  }

  const dynastyNames = activeDynasties
    .map((dynasty) => dynasty.chineseName)
    .join('、')
  return `${eventStatement}这是一项处于${dynastyNames}等政权并立阶段的${eventType}事件。`
}

export function buildHistoricalEvents(
  events: readonly IHistoricalEvent[],
  dynasties: readonly IDynasty[],
): readonly HistoricalEventWithDescription[] {
  return events.map((event) => ({
    ...event,
    description: event.description?.trim()
      ? event.description
      : createEventDescription(event, dynasties),
  }))
}

export const KEY_EVENTS = buildHistoricalEvents(RAW_KEY_EVENTS, DYNASTIES)
